package com.proyecto.entidades;

/**
 *
 * @author Arturo
 */
public class Empleado {
    
    private int emplId;
    private String empNombre;
    private String empPaterno;
    private String empMaterno;
    private String empFehaNacimiento;
    private int empTelefono;
    private int empCelular;
    private String empMail;
    private String empDireccion;
    private String empPuesto;
    private String empDepartamento;

    public int getEmplId() {
        return emplId;
    }

    public void setEmplId(int emplId) {
        this.emplId = emplId;
    }

    public String getEmpNombre() {
        return empNombre;
    }

    public void setEmpNombre(String empNombre) {
        this.empNombre = empNombre;
    }

    public String getEmpPaterno() {
        return empPaterno;
    }

    public void setEmpPaterno(String empPaterno) {
        this.empPaterno = empPaterno;
    }

    public String getEmpMaterno() {
        return empMaterno;
    }

    public void setEmpMaterno(String empMaterno) {
        this.empMaterno = empMaterno;
    }

    public String getEmpFehaNacimiento() {
        return empFehaNacimiento;
    }

    public void setEmpFehaNacimiento(String empFehaNacimiento) {
        this.empFehaNacimiento = empFehaNacimiento;
    }

    public int getEmpTelefono() {
        return empTelefono;
    }

    public void setEmpTelefono(int empTelefono) {
        this.empTelefono = empTelefono;
    }

    public int getEmpCelular() {
        return empCelular;
    }

    public void setEmpCelular(int empCelular) {
        this.empCelular = empCelular;
    }

    public String getEmpMail() {
        return empMail;
    }

    public void setEmpMail(String empMail) {
        this.empMail = empMail;
    }

    public String getEmpDireccion() {
        return empDireccion;
    }

    public void setEmpDireccion(String empDireccion) {
        this.empDireccion = empDireccion;
    }

    public String getEmpPuesto() {
        return empPuesto;
    }

    public void setEmpPuesto(String empPuesto) {
        this.empPuesto = empPuesto;
    }

    public String getEmpDepartamento() {
        return empDepartamento;
    }

    public void setEmpDepartamento(String empDepartamento) {
        this.empDepartamento = empDepartamento;
    }
    
    
}
